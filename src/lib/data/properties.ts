export const properties: Property[] = baseProperties.map((property) => ({
  ...property,
  images: [...property.images, ...additionalPropertyImages[property.id]],
}));

export function getProperty(id: string): Property | undefined {
  return properties.find((p) => p.id === id);
}

export function getPropertiesByDistrict(district: string): Property[] {
  return properties.filter((p) => p.district === district);
}

export function searchProperties(query?: string, district?: string): Property[] {
  let results = [...properties];
  if (district) {
    results = results.filter((p) => p.district === district);
  }
  if (query) {
    const q = query.toLowerCase();
    results = results.filter(
      (p) =>
        p.title.toLowerCase().includes(q) ||
        p.titleVi.toLowerCase().includes(q) ||
        p.address.toLowerCase().includes(q) ||
        p.district.includes(q)
    );
  }
  return results;
}

